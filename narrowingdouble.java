class narrowingdouble
{
	public static void main(String[]args)
	{
		double a=1;
		float b=(float) a;
		System.out.println(b);
		long c=(long) a;
		System.out.println(c);
		int d=(int) a;
		System.out.println(d);
		char g=(char) a;
		System.out.println(g);
		short i=(short) a;
		System.out.println(i);
		byte f=(byte) a;
		System.out.println(f);
	}
}